// Adição (+)
let resultado = 1 + 10;

// Subtração (-)
resultado = 5 - 3

// Multiplicação (*)
resultado = 10 * 3

// Divisão (/)
resultado = 10 / 5

// Módulo (%) - Pega o resto da divisão do primeiro valor pelo segundo
resultado = 15  % 4;


// Incremento (++)
let numero = 10;

numero++;

// Decremento (--)
numero = 20;

numero --;

// Exponenciação (**) > Eelva o primeiro valor à potência do segundo.
let valor = 2 ** 4 // 2 Elevado à potência de 4

console.log(valor);